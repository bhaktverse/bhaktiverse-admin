**प्रसिद्ध श्लोक (Key Verse 5.10):**
ब्रह्मण्याधाय कर्माणि सङ्गं त्यक्त्वा करोति यः।
लिप्यते न स पापेन पद्मपत्रमिवाम्भसा॥ ५.१० ॥

हिंदी अनुवाद:
जो पुरुष सब कर्मों को ब्रह्म में अर्पण करके और आसक्ति को त्यागकर कर्म करता है, वह पाप से उसी प्रकार लिप्त नहीं होता जैसे कमल का पत्ता जल से लिप्त नहीं होता।

English Translation:
One who performs his duty without attachment, surrendering the results unto the Supreme, is not affected by sinful action, as the lotus leaf is untouched by water.

**प्रसिद्ध श्लोक (Key Verse 5.18):**
