Destruction of Tripura:
Tarakasura''s three sons - Tarakaksha, Kamalaksha and Vidyunmali obtained three cities through penance - of gold, silver and iron. These three aligned once every thousand years. Shiva destroyed all three cities with a single arrow. Hence Shiva is called ''Tripurari''.

