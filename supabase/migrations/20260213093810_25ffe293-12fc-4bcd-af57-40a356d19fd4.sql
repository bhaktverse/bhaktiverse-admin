Descent of Ganga:
King Bhagiratha performed penance to bring Ganga from heaven to earth for the salvation of his ancestors. Earth could not bear Ganga''s force, so Shiva held Ganga in his matted locks. Hence Shiva is called ''Gangadhar''.

