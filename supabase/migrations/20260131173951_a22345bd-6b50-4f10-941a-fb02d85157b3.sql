**प्रसिद्ध श्लोक २.४७ (Most Famous Verse 2.47):**

कर्मण्येवाधिकारस्ते मा फलेषु कदाचन।
मा कर्मफलहेतुर्भूर्मा ते सङ्गोऽस्त्वकर्मणि॥ २.४७ ॥

हिंदी अनुवाद:
तेरा कर्म करने में ही अधिकार है, फलों में कभी नहीं। तू कर्मफल का हेतु मत बन और कर्म न करने में भी तेरी आसक्ति न हो।

English Translation:
You have the right to perform your duty, but never to its fruits. Do not be the cause of the results of action, nor be attached to inaction.

