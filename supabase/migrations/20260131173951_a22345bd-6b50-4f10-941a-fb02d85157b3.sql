जैसे इस देह में देही (आत्मा) बाल्यावस्था, यौवन और वृद्धावस्था को प्राप्त होता है, वैसे ही अन्य देह की प्राप्ति होती है। धीर पुरुष इससे मोहित नहीं होता।

English Translation:
There was never a time when I did not exist, nor you, nor these kings. Nor shall we cease to exist in the future.

Just as the embodied soul passes through childhood, youth and old age in this body, similarly it passes to another body. The wise person is not deluded by this.

